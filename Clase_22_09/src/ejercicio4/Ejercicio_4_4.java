package ejercicio4;

public class Ejercicio_4_4 {
    //operaciones con números enteros, y luego crea un programa que muestre el
    //Calcula "a mano" el resultado de las siguientes
    //resultado.
    public static void ejercicio_4_4_1(){
        double a = 5;
        System.out.println("El valor actual de la variable A es: "+a);

        a++;
        System.out.println("\nEl valor actual de la variable A después de aplicar ++ es: "+a);

        a*=2;
        System.out.println("\nEl valor actual de la variable A después de aplicar *=2 es: "+a);

        a-=3;
        System.out.println("\nEl valor actual de la variable A después de aplicar -=3 es: "+a);
        
        a%=5;
        System.out.println("\nEl valor actual de la variable A después de aplicar %=5 es: "+a);
        
        a=a+7;
        System.out.println("\nEl valor actual de la variable A después de aplicar =a+7 es: "+a);
        
    }
}
